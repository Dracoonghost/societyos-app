function formatDate(iso?: string): string {
  if (!iso) return "—";
  try {
    return new Intl.DateTimeFormat("en-US", {
      month: "short", day: "numeric", year: "numeric",
    }).format(new Date(iso));
  } catch {
    return iso;
  }
}

function healthColor(label?: string | null): string {
  if (!label) return "text-[var(--text-3)]";
  const l = label.toLowerCase();
  if (l.includes("excellent") || l.includes("good")) return "text-emerald-400";
  if (l.includes("needs work")) return "text-amber-400";
  if (l.includes("critical")) return "text-red-400";
  return "text-[var(--accent-amber)]";
}

function buildCopySummary(brand: BrandReportProps["brand"], summary: BrandSummary | null | undefined): string {
  const lines: string[] = [
    `Brand Analysis: ${brand.brand_name || brand.brand_url}`,
    `Health: ${summary?.health_score_label ?? "—"}`,
    "",
    summary?.tldr ?? "",
  ];
  if (summary?.top_strengths?.length) {
    lines.push("", "Strengths:", ...summary.top_strengths.map((s) => `• ${s.strength}`));
  }
  if (summary?.critical_weaknesses?.length) {
    lines.push("", "Weaknesses:", ...summary.critical_weaknesses.map((w) => `• ${w.weakness}`));
  }
  if (summary?.immediate_actions?.length) {
    lines.push("", "Immediate Actions:", ...summary.immediate_actions.map((a, i) => `${i + 1}. ${a}`));
  }
  lines.push("", `Generated by SocietyOS — ${brand.brand_url}`);
  return lines.filter((l, i) => !(l === "" && (lines[i - 1] === "" || i === 0 || i === lines.length - 1))).join("\n");
}

