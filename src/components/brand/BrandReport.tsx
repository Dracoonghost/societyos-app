interface Competitor {
  name: string;
  url?: string | null;
  description?: string | null;
  why_competitive?: string | null;
  evidence_source?: string | null;
}

interface CompetitorReport {
  id: string;
  competitor_url: string;
  competitor_name?: string | null;
  status: string;
  created_at: string;
}

interface BrandSummary {
  health_score_label?: string | null;
  tldr?: string | null;
  top_strengths?: { strength: string; detail?: string }[];
  critical_weaknesses?: { weakness: string; detail?: string }[];
  biggest_opportunities?: { opportunity: string; detail?: string }[];
  top_risks?: { risk: string; detail?: string }[];
  immediate_actions?: string[];
  data_gaps?: string[];
}

interface BrandReportProps {
  brand: {
    id: string;
    brand_url: string;
    brand_name?: string | null;
    brand_logo_url?: string | null;
    category?: string | null;
    completed_at?: string;
    analysis?: Record<string, Record<string, unknown> | null>;
    extracted_signals?: {
      seo_signals?: { sitemap_route_count?: number | null; [key: string]: unknown } | null;
      pagespeed?: unknown;
      tech_signals?: unknown;
    } | null;
    share_token?: string | null;
    competitors_discovered?: Competitor[];
    sections_generated?: string[];
  };
  linkedReports: CompetitorReport[];
  brandId: string;
  token: string;
  apiBase: string;
  onRefresh: () => void;
  onDelete: () => void;
  onNavigate: (reportId: string) => void;
  refreshing?: boolean;
  deleting?: boolean;
  refreshError?: string | null;
  readOnly?: boolean;
}

